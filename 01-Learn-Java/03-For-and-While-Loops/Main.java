public class Main {
    public static void main(String[] args){

        //For Loop = For each iteration do this
        for (int i = 0; i < 10; i++){

            //remainder(%)
            if (i % 2 == 0) {

                System.out.println(i + " is a multiple of 2 " );
            }
        }


        //while Loop
        int i = 0;
        while( i < 5 ){
            System.out.println(" Counting.. " + i);
            i++;
        }
    }
}